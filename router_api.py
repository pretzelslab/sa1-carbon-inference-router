ELECTRICITY_MAPS_API_KEY = os.getenv("ELECTRICITY_MAPS_API_KEY") or None

# Read budget caps from the same YAML the rest of CAIR uses
def _load_budget_caps() -> tuple[float, float]:
    registry_path = Path(__file__).parent / "model_registry.yaml"
    try:
        data = yaml.safe_load(registry_path.read_text())
        caps = data.get("carbon_budget", {})
        return caps.get("daily_gCO2", 500.0), caps.get("monthly_gCO2", 12000.0)
    except Exception:
        return 500.0, 12000.0

DAILY_CAP, MONTHLY_CAP = _load_budget_caps()

# Collect all unique zones from all models so we can fetch carbon for them all
def _all_zones() -> list[str]:
    registry_path = Path(__file__).parent / "model_registry.yaml"
    try:
        data = yaml.safe_load(registry_path.read_text())
        seen, zones = set(), []
        for model in data.get("models", []):
            for z in model.get("preferred_regions", []):
                if z not in seen:
                    seen.add(z)
                    zones.append(z)
        return zones
    except Exception:
        return ["US-EAST", "EU-WEST"]

ALL_ZONES = _all_zones()


