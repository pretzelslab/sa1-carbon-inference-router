"""
router_api.py — CAIR Phase 2: FastAPI HTTP wrapper

What this file does:
  Wraps the existing CAIR Python library in a web server.
  After running this file, any app (browser, curl, another service) can
  call CAIR over HTTP instead of importing it as a Python module.

  Three endpoints (Step 1 = first two only):
    GET  /health         → is the server alive? what's the cache age?
    POST /route          → get a routing decision (no actual LLM call)
    POST /route-and-call → routing decision + real LLM response (Step 2)

How to start the server:
  cd "AI Safety/sa1-carbon-inference-router"
  uvicorn router_api:app --reload --port 8000

Then open in browser:
  http://localhost:8000/docs   ← interactive API docs (auto-generated by FastAPI)
  http://localhost:8000/health ← live health check

How FastAPI works (one paragraph):
  FastAPI is a Python library that turns your functions into HTTP endpoints.
  You decorate a function with @app.get("/health") and FastAPI handles all
  the HTTP machinery (parsing requests, validating JSON, sending responses).
  It also auto-generates an interactive docs page (/docs) so you can test
  every endpoint from a browser without writing any frontend code.
"""

